    /**
     * Filles combobox with all data sets from chosen project
     */
    private void fillDataSets() throws Exception {
        try {
            //get current project
            NeurophProject project = CurrentProject.getInstance().getCurrentProject();
            //fill combobox with data set
            for (FileObject fo : project.getProjectDirectory().getFileObject(NeurophProject.TRAINING_SETS_DIR).getChildren()) {
                jComboBoxDataSet.addItem(fo.getName());
            }
            if (jComboBoxDataSet.getItemCount() == 0) {
                throw new Exception("No data set in project");
            }
        } catch (Exception ex) {

            throw ex;
        }

    }
